# -----------------------------------------------Length of String-----------------------------------------------
# Description: This function returns the length of the string
# Parameters: String
# Return Type: Integer
# Time Complexity Worst Case: O(n)
# ----------------------------------------------------------------------------------------------------------------
fruits = "mango"
len1 = len(fruits)
print(len1)  # 5
# -----------------------------------------------Concatenation of String-----------------------------------------------
# Description: This function returns the concatenation of two strings
# Parameters: String
# Return Type: String
# Time Complexity Worst Case: O(n)
# ----------------------------------------------------------------------------------------------------------------
fruits = "mango"
fruits1 = "apple"
fruits2 = fruits + fruits1
print(fruits2)  # mangoapple
# -----------------------------------------------Repetition of String-----------------------------------------------
# Description: This function returns the repetition of string
# Parameters: String
# Return Type: String
# Time Complexity Worst Case: O(n)
# ----------------------------------------------------------------------------------------------------------------
fruits = "mango"
fruits1 = fruits * 2
print(fruits1)  # mangomango
# -----------------------------------------------Indexing of String-----------------------------------------------
# Description: This function returns the character at the given index
# Parameters: String
# Return Type: Character
# Time Complexity Worst Case: O(1)
# ----------------------------------------------------------------------------------------------------------------
fruits = "mango"
fruits1 = fruits[0]
print(fruits1)  # m
# -----------------------------------------------Slicing of String-----------------------------------------------
# Description: This function returns the character at the given index
# Parameters: String
# Return Type: Character
# Time Complexity Worst Case: O(n)
# ----------------------------------------------------------------------------------------------------------------
fruits = "mango"
fruits1 = fruits[0:3]
