# Number Type
# Integer Int, or integer, is a whole number, positive or negative, without decimals, of unlimited length.
a = 10
print(a)
print("I am ", type(a), " type")
# Float
# Float, or "floating point number" is a number, positive or negative, containing one or more decimals.
b = 10.5
print(b)
print("I am ", type(b), " type")
# Complex
# Complex numbers are written with a "j" as the imaginary part.
# formula for complex number is a + bj where a is real part and b is imaginary part and j is imaginary unit.
c = 1 + 2j
print(c)
print("I am ", type(c), " type")
# Boolean
#  There are two boolean values: True and False.
d = True  # Note that "True" and "False" must be capitalized.
print(d)
print("I am ", type(d), " type")
# String
# String is a sequence of Unicode characters. We can use single quotes or double quotes to represent strings.
e = "Raj"
print(e)
print("I am ", type(e), " type")
# List
# Ordered collection of items and are mutable. Allows duplicate members and is indexed and changeable.
f = [1, 2, 3, 4, 5, ["Cat", "Dog"]]
print(f)
print("I am ", type(f), " type")
# Tuple
# Ordered collection of items and are immutable. Allows duplicate members and is indexed and unchangeable.
g = (1, 2, 3, 4, 5)
print(g)
print("I am ", type(g), " type")
# Dictionary
# Unordered collection of items and are mutable. No duplicate members and is indexed and changeable.
h = {"name": "Raj", "age": 20}
print(h)
print("I am ", type(h), " type")
# Set
# Unordered collection of items and are mutable. No duplicate members and is indexed and changeable.
i = {1, 2, 2, 3, 4, 5}
print(i)
print("I am ", type(i), " type")
