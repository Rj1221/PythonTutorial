# List of Operators in Python

# Arithmetic Operators

# Addition +
a = 10 + 20
# Subtraction -
b = 20 - 10
# Multiplication *
c = 10 * 20
# Division /
d = 20 / 10
# Modulus %
e = 20 % 10  # Remainder
# Exponent **
f = 10**2  # 10^2
# Floor Division // used to get the quotient
g = 20 // 10  # Quotient

a = divmod(9, 5)  # It is used to return the quotient and remainder of the division
print(a)  # (1, 4)

# Assignment Operators

# Equal =
h = 10
# Add and =
h += 10  # h=h+10
# Subtract and =
h -= 10  # h=h-10
# Multiply and =
h *= 10  # h=h*10
# Divide and =
h /= 10  # h=h/10
# Modulus and =
h %= 10  # h=h%10
# Exponent and =
h **= 10  # h=h**10
# Floor Division and =
h //= 10  # h=h//10

# Comparison Operators

# Equal ==
if 10 == 10:
    print("True")
# Not Equal !=
if 10 != 20:
    print("True")
# Greater Than >
if 10 > 5:
    print("True")
# Less Than <
if 10 < 20:
    print("True")
# Greater Than or Equal To >=
if 10 >= 10:
    print("True")
# Less Than or Equal To <=
if 10 <= 20:
    print("True")

# Logical Operators

# And and
if 10 == 10 and 20 == 20:
    print("True")
# Or or
if 10 == 10 or 20 == 20:
    print("True")
# Not not
if not 10 == 20:
    print("True")
